package aula0423.Ex1;

public class Matematica {
	private Matematica () {
		
	}
	
	public static double soma(int a, int b) {
		return a + b;
	}
	
	public static double soma(double a, double b) {
		return a + b;
	}
	
	public static double soma(double a, int b) {
		return a + b;
	}
	
	public static double soma(int a, double b) {
		return a + b;
	}
	
	public static double subtracao(int a, int b) {
		return a - b;
	}
	
	public static double subtracao(double a, double b) {
		return a - b;
	}
	
	public static double subtracao(double a, int b) {
		return a - b;
	}
	
	public static double subtracao(int a, double b) {
		return a - b;
	} 
}
